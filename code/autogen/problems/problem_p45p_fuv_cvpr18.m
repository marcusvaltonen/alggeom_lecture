for k = 1:5
    M = [M; U(:,k)' zeros(1,3) -u(1,k)*U(:,k)' 1 0 -u(1,k)];
    M = [M; zeros(1,3) U(:,k)' -u(2,k)*U(:,k)' 0 1 -u(2,k)];
